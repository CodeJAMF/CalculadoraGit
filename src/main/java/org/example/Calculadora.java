package org.example;

public class Calculadora {

    public double sumar (double a, double b){
        return a+b;
    }

    public double restar (double a, double b){
        return a-b;
    }

    public double division (double a, double b){
        return a/b;
    }

    public double multiplica (double a, double b){
        return a*b;

    }


}
